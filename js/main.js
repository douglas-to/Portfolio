// Scroll Effect

function scrollEffect(){
	
	window.sr = ScrollReveal({ reset: true });

	sr.reveal(".section1", { delay: 250 });
	sr.reveal(".section2", { delay: 250 });
	sr.reveal(".section3", { delay: 250 });
	sr.reveal(".section4", { delay: 250 });
	sr.reveal(".footer", { delay: 250 });	
}

scrollEffect();

